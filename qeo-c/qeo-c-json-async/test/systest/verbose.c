#include <stdlib.h>
#include <string.h>

int verbose(void)
{
    static int _verbose = -1;

    if (-1 == _verbose) {
        const char *s = getenv("VERBOSE");

        _verbose = 0;
        if ((NULL != s) && (0 == strcmp(s, "1"))) {
            _verbose = 1;
        }
    }
    return _verbose;
}
